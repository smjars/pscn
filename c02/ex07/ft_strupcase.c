char	*ft_strupcase(char *str)
{
	int	cn;

	cn = 0;
	while (str[cn] != '\0')
	{
		if (str[cn] > 96 && str[cn] < 123)
			str[cn] -= 32;
		cn++;
	}
	cn = 0;
	return (str);
}
