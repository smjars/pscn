#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	cnt;

	(void)argc;
	cnt = 0;
	while (argv[0][cnt] != '\0')
	{
		write(1, &argv[0][cnt], 1);
		cnt++;
	}
	write(1, "\n", 1);
	return (0);
}
